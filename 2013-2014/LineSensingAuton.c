task readSensors() {
	dir_left = HTIRS2readACDir(irL);
	HTIRS2readAllDCStrength(irL, S1_left, S2_left, S3_left, S4_left, S5_left);
	HTCSreadRGB(lineL, redL, greenL, blueL);
	HTCSreadRGB(lineR, redR, greenR, blueR);
}

task main() {
	disableDiagnosticsDisplay();
  initializeRobot();
  StartTask(readSensors);
  waitForStart();
  driveToLine();
  followLine();
  driveToEnd();
  turnOntoRamp();
}

void driveToLine() {
	nMotorEncoder[driveL] = 0;
	nMotorEncoder[driveR] = 0;
	while( (abs(nMotorEncoder[driveL]) + abs(nMotorEncoder[driveR])) / 2.0 <= driveToLineLength) {
		motor[driveL] = motor[driveR] = 100;
	}
	motor[driveL] = motor[driveR] = 0;
}

void followLine() {
	while(S3_left < 70) {
		if(leftOn() && rightOn()) {
			motor[driveL] = motor[driveR] = 100;
		} else if(leftOn() || rightOn()) {
			if(leftOn()) {
				motor[driveR] = 50;
				motor[driveL] = 0;
			} else {
				motor[driveL] = 50;
				motor[driveR] = 0;
			}
		}
	}

	motor[driveL] = motor[driveR] = 0;
	score();
}

void score() {
	servo[autoArm] = 255;
	wait10Msec(100);
	servo[autoArm] = 10;
	wait10Msec(100);
}

void driveToEnd() {
	while(leftOn() || rightOn()) {
		if(leftOn() && rightOn()) {
			motor[driveL] = motor[driveR] = 100;
		}
		if(leftOn() && !rightOn()) {
			motor[driveL] = 0;
			motor[driveR] = 50;
		}
		if(!leftOn() && rightOn()) {
			motor[driveL] = 50;
			motor[driveR] = 0;
		}
	}
	motor[driveL] = motor[driveR] = 0;
}

