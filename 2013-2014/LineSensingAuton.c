void startSensors(tHTIRS2DSPMode mode) {
	while(true) {
		PlaySound(soundShortBlip);
		if(HTIRS2setDSPMode(irL, mode)) {
			break;
		}
	}
}

bool leftOn() {
	if(redL > 100 || blueL > 100) {
		return true;
	}
	return false;
}

bool rightOn() {
	if(redR > 100 || blueL > 100) {
		return true;
	}
	return false;
}
